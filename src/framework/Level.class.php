/**
 * Struct representing enabled logging levels.
 * 
 * @package framework
 */
class Level {
	
	/**
	 * Default logging level.
	 */
	const Debug = 0;
	
	/**
	 * Info logging level.
	 */
	const Info = 1;
	
	/**
	 * Warning logging level.
	 */
	const Warning = 2;
	
	/**
	 * Error logging level.
	 */
	const Error = 3;
	
	/**
	 * Critical logging level.
	 */
	const Critical = 4;

}

?>